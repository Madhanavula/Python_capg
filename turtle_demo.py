import turtle as t
import time
t.bgcolor("white")
t.pensize(2)
for i in range(4):
    t.forward(100)
    t.right(90)

for i in range(4):
    t.left(90)
    t.forward(100)

for i in range(4):
    t.right(90)
    t.forward(100)

t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
    
time.sleep(5)